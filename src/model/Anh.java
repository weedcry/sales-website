package model;

public class Anh {
	private String tenanh;
	private String link;

	
	public Anh() {
	}
	public Anh(String tenanh, String link) {
		this.tenanh = tenanh;
		this.link = link;
	}
	public String getTenanh() {
		return tenanh;
	}
	public void setTenanh(String tenanh) {
		this.tenanh = tenanh;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	
	
	
}
